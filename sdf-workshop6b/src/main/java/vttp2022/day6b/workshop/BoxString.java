package vttp2022.day6b.workshop;

public class BoxString {
    private String content;

    public BoxString() {
        // empty constructor
    }

    public BoxString(String pContent) {
        this.content = pContent;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String pContent) {
        this.content = pContent;
    }
}
